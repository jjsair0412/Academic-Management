package Service.Info;

public class Professor_Join_info {
	private String id;
	private String name;
	private int age;
	private String sex;
	private String major;
	private int proPw;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getMajor() {
		return major;
	}
	public void setMajor(String major) {
		this.major = major;
	}
	public int getProPw() {
		return proPw;
	}
	public void setProPw(int proPw) {
		this.proPw = proPw;
	}
	
}
