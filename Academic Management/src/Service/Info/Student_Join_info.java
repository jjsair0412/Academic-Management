package Service.Info;

public class Student_Join_info {
	private String id;
	private int number;
	private String name;
	private int age;
	private String sex;
	private String major;
	private int StdPw;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getMajor() {
		return major;
	}
	public void setMajor(String major) {
		this.major = major;
	}
	public int getStdPw() {
		return StdPw;
	}
	public void setStdPw(int stdPw) {
		StdPw = stdPw;
	}

}
